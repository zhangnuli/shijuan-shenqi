def unit(uid: str, name: str, lessons: list[str], points: list[str]) -> dict:
    return {"id": uid, "name": name, "lessons": lessons, "points": points}


def pack(
    subject: str,
    edition: str,
    grade: int,
    semester: str,
    title: str,
    units: list[dict],
    edition_label: str,
    subject_label: str,
    extra_hints: list[str] | None = None,
) -> dict:
    sem_label = "上册" if semester == "shang" else "下册"
    hints = [
        "命题必须落在本册单元/课时范围内，禁止超纲",
        "单元测试只考所选单元课时与知识点",
        "期中覆盖前半册单元，期末覆盖全册",
        f"出题风格可参考智慧教育平台「同步课堂」「基础性作业」：{PLATFORM['classroomUrl']}",
    ]
    if extra_hints:
        hints.extend(extra_hints)
    return {
        "subject": subject,
        "edition": edition,
        "grade": grade,
        "semester": semester,
        "title": title,
        "source": {
            **PLATFORM,
            "editionLabel": edition_label,
            "subjectLabel": subject_label,
            "smarteduPathHint": f"小学 → {grade}年级 → {subject_label} → {edition_label} → {sem_label}",
        },
        "units": units,
        "examHints": hints,
    }


